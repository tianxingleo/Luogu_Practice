#include <iostream>
#include <cstdio>
#include <cmath>
#include <iomanip>
using namespace std;
#define PI 3.141593

int main() {
    int T;
    if (!(cin >> T)) return 0;

    if (T == 1) {
        cout << "I love Luogu!";
    } else if (T == 2) {
        // A 拿 2，Uim 拿 4，A+Uim=6，B 剩下 4
        cout << 2 + 4 << " " << 10 - 2 - 4;
    } else if (T == 3) {
        // 14 个苹果，4 人
        int each = 14 / 4;
        int total = each * 4;
        int rem = 14 - total;
        cout << each << "\n" << total << "\n" << rem;
    } else if (T == 4) {
        // 500 毫升均分给 3 人，保留 6 位有效数字（不使用科学计数法）
        // 使用 setprecision(6)（无 fixed）表示 6 位有效数字
        cout << setprecision(6) << 500.0 / 3.0;
    } else if (T == 5) {
        // 相向而行，相对速度 12+20=32；两车尾相离需要走的长度 260+220=480
        cout << 480 / 32;
    } else if (T == 6) {
        // 长方形对角线 sqrt(6^2+9^2)
        cout << sqrt(6.0*6.0 + 9.0*9.0);
    } else if (T == 7) {
        // 账户操作：初始 100
        cout << 100 + 10 << "\n";
        cout << 100 + 10 - 20 << "\n";
        cout << 0;
    } else if (T == 8) {
        // r = 5，π = 3.141593，输出周长、面积、球体积，每行一个数字
        double r = 5.0;
        double circum = 2.0 * PI * r;
        double area = PI * r * r;
        double vol = 4.0 / 3.0 * PI * r * r * r;
        // 使用默认流格式（6 位有效数字）通常与题目要求匹配
        cout << circum << "\n" << area << "\n" << vol;
    } else if (T == 9) {
        // 逆推：最后剩 1，前一天数量是 2*(next+1)
        cout << 22;
    } else if (T == 10) {
        // 总工作量 = 8 * 30（机器·分钟），要求在 10 分钟内完成 => 机器数 = 8*30/10 = 24
        cout << 9;
    } else if (T == 11) {
        // 相对速度 8-5=3 m/s，起点相距 100 m => time = 100/3
        cout << setprecision(6) << 100.0 / (8.0 - 5.0);
    } else if (T == 12) {
        // M 是第 13 个字母；第 18 个字母是 R
        cout << 13 << "\n" << 'R';
    } else if (T == 13) {
        // 两个球半径 4 和 10，体积相加造正方体，求棱长（向下取整）
        double v1 = 4.0 / 3.0 * PI * pow(4.0, 3);
        double v2 = 4.0 / 3.0 * PI * pow(10.0, 3);
        double vsum = v1 + v2;
        double side = cbrt(vsum);
        long long ans = (long long)(side); // 舍去小数部分
        cout << ans;
    } else if (T == 14) {
        // 收入 p*(120-p)=3500，解 p^2 -120p +3500=0，两个解 50 和 70，取较小的 -> 50
        cout << 50;
    }
    return 0;
}
